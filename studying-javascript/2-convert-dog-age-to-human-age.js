// How old will your dog be if it was human?

//create a prompt that asks the user the age of their dog.

let dogAge = prompt("How old is your dog?");

// Calculate the equivalent human age of dog using this formula 
const humanAge = (dogAge - 2) * 4 + 21;

// Give the answer back to the user through an alert message.
alert("Your dog is " + humanAge + " years old in human years.");
